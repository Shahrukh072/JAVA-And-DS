package Arrays;
public class BasicsOfArray {
	public static void main(String[] args) {
		int x[] = new int [10];
		x[0]= 5;
		x[5]= 17;
		System.out.println(x[5]);
		System.out.println(x[0]);
		System.out.println(x[3]);  
		
		char [] a= new char[10];
		double [] b = new double[10];
		System.out.println(a[0]);
		System.out.println(b[0]);
	}
}
