package Operators;

public class BitwiseOperators2 {
	public static void main(String[] args) {
		int a=10;
		System.out.println(a<<1);  //left shift  bitwise operator
		System.out.println(a<<2);
		System.out.println(a>>1);   // right shift bitwise operator
		System.out.println(a>>2);
	}

}
